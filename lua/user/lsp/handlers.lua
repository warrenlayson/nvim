M.lsp_format = function(bufnr)
  vim.lsp.buf.format {
    filter = function(client)
      return client.name == "null-ls"
    end,
    bufnr = bufnr,
    async = true,
  }
end

M.lsp_range_format = function(bufnr)
  vim.lsp.buf.range_formatting {
    filter = function(client)
      return client.name == "null-ls"
    end,
    bufnr = bufnr,
    async = true,
  }
end

